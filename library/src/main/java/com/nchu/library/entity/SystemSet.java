package com.nchu.library.entity;

public class SystemSet {
	private String cupBoardHelp;
	private String volunteerHelp;
	private String lectureHelp;
	private int sysId;
	public int getSysId() {
		return sysId;
	}
	public void setSysId(int sysId) {
		this.sysId = sysId;
	}
	public String getCupBoardHelp() {
		return cupBoardHelp;
	}
	public void setCupBoardHelp(String cupBoardHelp) {
		this.cupBoardHelp = cupBoardHelp;
	}
	public String getVolunteerHelp() {
		return volunteerHelp;
	}
	public void setVolunteerHelp(String volunteerHelp) {
		this.volunteerHelp = volunteerHelp;
	}
	public String getLectureHelp() {
		return lectureHelp;
	}
	public void setLectureHelp(String lectureHelp) {
		this.lectureHelp = lectureHelp;
	}


}
